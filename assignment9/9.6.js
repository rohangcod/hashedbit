// Person object
const person = {
    name: "Rohan",
    age: 21,
    occupation: "Software Developer"
  };
  
  // Function to print greeting
  function greetPerson(personObj) {
    console.log(`Hello, my name is ${personObj.name}. I am ${personObj.age} years old and I work as a ${personObj.occupation}.`);
  }
  
  // Function call
  greetPerson(person);
  