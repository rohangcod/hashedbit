// Function to get object keys
function getObjectKeys(obj) {
    return Object.keys(obj);
  }
  
  // Example object
  const person = {
    name: "Rohan",
    age: 21,
    occupation: "Software Engineer",
    country: "India"
  };
  
  // Function call and output
  console.log("Object keys:", getObjectKeys(person));
  